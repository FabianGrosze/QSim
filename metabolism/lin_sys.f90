subroutine lin_sys(a11,a12,a21,a22,b1,b2,x1,x2)
   
   implicit none
   
   double precision  :: a11,a12,a21,a22,b1,b2,det
   real              :: x1, x2
   
   det = (a11*a22-a12*a21)
   if (det /= 0.0) then
      x1 = (a22*b1-a12*b2)/det
      x2 = (a11*b2-a21*b1)/det
   endif
   
   return
end subroutine lin_sys